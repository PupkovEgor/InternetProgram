<?php

$N = rand(1, 10);
$c1 = rand(1, 5);
$c2 = rand(6, 10);


echo('Массив, состоящий из '.$N.' элементов: ');
for ($i=0; $i<=$N-1; $i++){
    $A[$i] = rand(1, 10);
    echo($A[$i].' ');
}
echo ('<br>');

echo('Интервал равен ['.$c1.', '.$c2.']'.'<br>');

$min = min($A);
echo ('Минимальный элемент равен '.$min.'<br>');

echo('Новый массив: ');
for ($i=0; $i<=$N-1; $i++){
    if ($A[$i] >= $c1 and $A[$i] <= $c2) $A[$i] = $min;
    echo($A[$i].' ');
} 

?>